pub fn get_dna_idx_from_u8(s: u8) -> usize {
    ASCII_TO_ACGTN_IDX[s as usize]
}

pub fn complement(s: &u8) -> u8 {
    COMP_TRANS[*s as usize]
}

pub fn revcomp(seq: &mut [u8]) {
    seq.iter_mut().for_each(|c| *c = complement(c));
    seq.reverse();
}

